<h6>7. Comunicación de datos personales</h6>
<p>Informamos a los Usuarios que los datos personales nos faciliten no serán comunicados a ningún tercero salvo autorización expresa. Sí que es conveniente que sepan los Usuarios que sus datos personales podrán ser comunicados a las entidades administrativas, autoridades judiciales y/o policiales, siempre y cuando esté establecido por Ley.</p>


 <h6>8. Seguridad de los datos personales</h6>


 <p>LA ASOCIACIÓN tiene implementadas todas las medidas de índole técnica y organizativa necesarias para garantizar la seguridad de los datos personales y evitar su alteración, pérdida y tratamiento y/o acceso no autorizado, teniendo en cuenta el estado de la tecnología, naturaleza de los datos almacenados y riesgos a que están expuestos, ya sea que provengan de la acción humana, del medio físico o natural, tal y como establece la legislación peruana vigente de protección de datos personales.<br>
También tenemos implementadas medidas de seguridad adicionales para reforzar la confidencialidad e integridad de la información y continuamente mantiene la supervisión, control y evaluación de los procesos para asegurar la privacidad de los datos personales. Sin embargo, la transmisión de información mediante las redes de comunicación y de Internet no es totalmente segura; por eso, y a pesar de que se realizará los mejores esfuerzos para proteger los datos personales, no se puede garantizar la seguridad de estos durante el tránsito hasta los sitios Web. En tal sentido, toda la información que los Usuarios proporcionen, se enviará por su cuenta y riesgo. Por ello LA ASOCIACIÓN recomienda la máxima diligencia a sus Usuarios cuando trasladen a terceros o publiquen información personal para evitar poner en riesgo sus datos personales, eludiendo LA ASOCIACIÓN toda responsabilidad en caso de sustracciones, modificaciones o pérdidas de datos ilícitas.</p>




