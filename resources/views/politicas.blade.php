 <h6>6. Tratamiento de datos personales y consentimiento</h6>



<p>Los datos personales facilitados por los usuarios se almacenarán en los bancos de datos que forman parte del sistema de información de LA ASOCIACIÓN y serán tratados para poder llevar a cabo las finalidades expuestas anteriormente.<br>
