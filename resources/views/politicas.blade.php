        <h6>3. Legislación</h6>
        <p>Esta política está regulada por la Legislación peruana y en particular por:</p>

        <ul>
            <li>Ley N° 29733 – Ley de Protección de Datos Personales.</li>
            <li>Decreto Supremo N° 003-2013-JUS, que aprueba el Reglamento de la Ley N° 29733.</li>
            <li>Directiva de Seguridad de la Información, aprobada por la Resolución Directoral N° 019-2013-JUS/DGPDP.</li>

        </ul>

        <p>LA ASOCIACIÓN, desarrolla su política de tratamiento de datos personales en atención a los principios rectores establecidos en la Ley N° 29733 - Ley de Protección de Datos Personales y, por lo tanto:</p>


        <ul>
            <li>De acuerdo con el principio de legalidad, rechaza la recopilación de los datos personales de nuestros Usuarios por medios fraudulentos, desleales o ilícitos.</li>
            <li>Conforme al principio de consentimiento, en el tratamiento de los datos personales de nuestros Usuarios mediará su consentimiento.</li>
            <li>Los datos personales de nuestros Usuarios se recopilarán para un fin determinado, explícito y lícito, y no se extenderá a otra finalidad que no sea la establecida de manera inequívoca como tal al momento de su recopilación, excluyendo los casos de actividades de valor histórico, estadístico o científico cuando se utilice un procedimiento de disociación o anonimización.</li>

            <li>Los datos personales que vayan a ser tratados serán veraces, exactos, actualizados en la medida de lo posible, necesarios, pertinentes y adecuados respecto de la finalidad para la que fueron recopilados. Se conservarán de forma tal que se garantice su seguridad y solo por el tiempo necesario para cumplir con la finalidad del tratamiento.</li>

             <li>LA ASOCIACIÓN y, en su caso, los encargados de tratamiento, adoptan las medidas técnicas, organizativas y legales necesarias para garantizar la seguridad y confidencialidad de los datos personales. LA ASOCIACIÓN cuenta con las medidas de seguridad apropiadas, y acordes con el tratamiento que se vaya a efectuar y con la categoría de datos personales de que se trate.</li>

             <li>LA ASOCIACIÓN informa a sus Usuarios que pueden ejercer su derecho a la protección de datos personales contenido en el artículo 2 inciso 6) de la Constitución de 1993, ante la Autoridad Nacional de Protección de Datos y en sede jurisdiccional ante el Poder Judicial a los efectos del inicio del correspondiente proceso de habeas data.</li>

        </ul>

