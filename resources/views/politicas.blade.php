        <h6>2. Objetivos y finalidad</h6>
        
        <p>
            
           LA ASOCIACIÓN es consciente de la elevada consideración que tiene la privacidad de nuestros clientes y de todas aquellas personas que se interesan por nuestros productos y servicios. Siendo consecuentes con esta consideración tenemos el compromiso de respetar su privacidad y proteger la confidencialidad de su información privada y datos personales. Y es por ello que, el objetivo de esta política de privacidad es dar a conocer a nuestros Usuarios la manera en que se recogen, se tratan y se protegen los datos personales, definida según la Ley de Protección de Datos Personales - Ley N° 29733, que a través de Internet son introducidos en los sitios Web a su disposición.<br>
